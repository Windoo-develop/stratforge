export type MessageKey = keyof typeof messages.en

export function translate(locale: AppLocale, key: MessageKey, values?: Record<string, string | number>) {
  const template = String(messages[locale][key] ?? messages.en[key] ?? key)

  if (!values) return template

  return Object.entries(values).reduce<string>((result, [token, value]) => {
    return result.replaceAll(`{{${token}}}`, String(value))
  }, template)
}
